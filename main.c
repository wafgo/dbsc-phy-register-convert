static uint16_t rset_offsets[] = {
    0x200,
    0x800,
    0xa00,
    0xa80,
    0xb80,
};

#define ARRAY_SIZE(x) (sizeof(x)/sizeof(x[0]))

static uint16_t reg_value_to_reg_idx(uint16_t reg_value, uint16_t *rel_offset)
{
    int i;
    for(i = (ARRAY_SIZE(rset_offsets) - 1); i >= 0; i--) {
        uint16_t offset = rset_offsets[i];
        if (reg_value >= offset) {
            break;
        }
    }
    if (rel_offset) {
        *rel_offset = reg_value - rset_offsets[i];
    }
    return i;
}

int main(void)
{
    int i;
    for(i = 0; i < ARRAY_SIZE(phy_regs_r8a7796); ++i) {
        uint32_t reg_def = phy_regs_r8a7796[i];
        uint16_t reg_value;
        uint16_t reg_set_idx;
        uint16_t reg_rel_offset = 0;
        uint8_t bf_len;
        uint8_t lsb;

        if (reg_def == 0xffffffff) {
            reg_set_idx = 5;
            reg_value = 0xffff;
            bf_len = 0;
            lsb = 0;
        } else {
            reg_value = reg_def & 0xffff;
            reg_set_idx = reg_value_to_reg_idx(reg_value, &reg_rel_offset);
            bf_len = (reg_def >> 16) & 0xff;
            lsb = (reg_def >> 24) & 0xff;
        }
        printf("\t /* %s */ {. rset = %s, .rset_rel_num = %i, .lsb = %i, .bf_len = %i},\n", phy_function_names[i], rset_names[reg_set_idx], reg_rel_offset, lsb, bf_len);
    }
}
